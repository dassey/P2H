/*
 * deck-builder.js — turns extracted PPTX content + user options into ONE
 * self-contained HTML slideshow string (no external requests, works from
 * file://, survives email). Layout contract follows the project spec:
 * stacked full-screen <section class="slide">, one .active, fade transition,
 * a single showSlide(n) engine, keyboard/touch/button navigation, counters,
 * CSS-variable theme, optional animated canvas gated per slide via data-bg.
 *
 *   DeckBuilder.build(content, options) → html string
 *
 * All slide text is HTML-escaped at extraction time (runs) or here (titles,
 * cells); the only markup that reaches the file is generated by this module.
 */
