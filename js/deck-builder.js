  function merge(base, over) {
    var out = {};
    for (var k in base) {
      if (base[k] && typeof base[k] === 'object' && !Array.isArray(base[k])) {
        out[k] = merge(base[k], (over || {})[k] || {});
      } else {
        out[k] = over && over[k] !== undefined ? over[k] : base[k];
      }
    }
    return out;
  }

  // ---------------------------------------------------------------- blocks

  function bulletsHtml(items) {
    var out = '<ul class="bullets">';
    items.forEach(function (it) {
      var lvl = Math.min(it.level || 0, 4);
      if (it.noBullet) {
        out += '<li class="plain lvl' + lvl + '">' + it.html + '</li>';
      } else {
        out += '<li class="lvl' + lvl + '">' + it.html + '</li>';
      }
    });
    return out + '</ul>';
  }

  function tableHtml(block) {
    var rows = block.rows;
    var html = '<div class="tbl-wrap"><table>';
    rows.forEach(function (r, i) {
      var tag = (i === 0 && block.headerRow) ? 'th' : 'td';
      html += '<tr>';
      r.forEach(function (c) { html += '<' + tag + '>' + esc(c) + '</' + tag + '>'; });
      html += '</tr>';
    });
    return html + '</table></div>';
  }

  function stubHtml(label) {
    var icon = /chart/i.test(label) ? '📊' : /smartart|diagram/i.test(label) ? '🧩' : '🖼';
    return '<div class="stub">' + icon + ' ' + esc(label) +
      '<span class="stub-note">from the original deck</span></div>';
  }

  function blocksHtml(blocks) {
    var html = '', i = 0;
    while (i < blocks.length) {
      var b = blocks[i];
      if (b.kind === 'image') {
        // group consecutive images into one row
        var imgs = [];
        while (i < blocks.length && blocks[i].kind === 'image') { imgs.push(blocks[i]); i++; }
        html += '<div class="media-row n' + Math.min(imgs.length, 3) + '">';
        imgs.forEach(function (im) {
          html += '<figure><img src="' + im.src + '" alt="' + esc(im.name) + '"></figure>';
        });
        html += '</div>';
        continue;
      }
      if (b.kind === 'bullets') html += bulletsHtml(b.items);
      else if (b.kind === 'table') html += tableHtml(b);
      else if (b.kind === 'stub') html += stubHtml(b.label);
      i++;
    }
    return html;
  }

  function cornersHtml() {
    return '<div class="corner tl"></div><div class="corner tr"></div>' +
           '<div class="corner bl"></div><div class="corner br"></div>';
  }

  function slideHtml(slide, idx, opt, dataBg) {
    var cls = 'slide s-' + slide.type + (idx === 0 ? ' active' : '');
    var html = '<section class="' + cls + '" data-bg="' + dataBg + '">';
    if (slide.type === 'title') {
      html += '<div class="wrap center">';
      if (opt.flairCorners) html += cornersHtml();
      html += '<h1>' + esc(slide.title || opt.title) + '</h1>';
      if (slide.subtitle) html += '<p class="subtitle">' + esc(slide.subtitle).replace(/\n/g, '<br>') + '</p>';
      html += blocksHtml(slide.blocks);
      html += '</div>';
    } else if (slide.type === 'section') {
      html += '<div class="wrap center">';
      html += '<div class="section-num">' + String(idx + 1).padStart(2, '0') + '</div>';
      html += '<h2 class="section-title">' + esc(slide.title || '') + '</h2>';
      html += blocksHtml(slide.blocks);
      html += '</div>';
    } else {
      html += '<div class="wrap">';
      if (slide.title) html += '<h2>' + esc(slide.title) + '</h2>';
      if (slide.subtitle) html += '<p class="subtitle small">' + esc(slide.subtitle) + '</p>';
      html += '<div class="content">' + blocksHtml(slide.blocks) + '</div>';
      html += '</div>';
    }
    return html + '</section>';
  }

